static const char *ui_i18n_join2(const char *a, const char *b) {
  static char buffers[4][96];
  static unsigned idx;
  char *buf = buffers[idx++ % 4];
  snprintf(buf, sizeof(buffers[0]), "%s\n%s", a ? a : "", b ? b : "");
  return buf;
}

